/**
 * Resets passwords for E2E test accounts and writes tests/e2e/.env.test
 *
 * Usage:
 *   node scripts/reset-e2e-passwords.cjs
 *
 * Credentials (first match wins):
 *   1. scripts/service-account.json   ← download from Firebase Console →
 *                                        Project Settings → Service accounts →
 *                                        "Generate new private key"
 *   2. FIREBASE_PROJECT_ID + FIREBASE_PRIVATE_KEY + FIREBASE_CLIENT_EMAIL
 *      in environment (loaded from .env.vercel-prod if present)
 */
const admin = require('firebase-admin');
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const SA_PATH = path.resolve(__dirname, 'service-account.json');

let credential;

if (fs.existsSync(SA_PATH)) {
  const sa = JSON.parse(fs.readFileSync(SA_PATH, 'utf8'));
  credential = admin.credential.cert(sa);
  console.log('Using service-account.json');
} else {
  // Try loading from env
  require('dotenv').config({ path: path.resolve(__dirname, '../.env.vercel-prod') });
  const { FIREBASE_PROJECT_ID, FIREBASE_PRIVATE_KEY, FIREBASE_CLIENT_EMAIL } = process.env;

  if (!FIREBASE_PROJECT_ID || !FIREBASE_PRIVATE_KEY || !FIREBASE_CLIENT_EMAIL) {
    console.error(`
No Firebase Admin credentials found.

Option A (easiest): Download service account key from Firebase Console
  1. Go to https://console.firebase.google.com → teqf-website → Project Settings → Service accounts
  2. Click "Generate new private key" → download JSON
  3. Save as: scripts/service-account.json
  4. Re-run: node scripts/reset-e2e-passwords.cjs

Option B: Set FIREBASE_PROJECT_ID, FIREBASE_PRIVATE_KEY, FIREBASE_CLIENT_EMAIL in environment.
`);
    process.exit(1);
  }

  credential = admin.credential.cert({
    project_id: FIREBASE_PROJECT_ID,
    private_key: FIREBASE_PRIVATE_KEY.replace(/\\n/g, '\n'),
    client_email: FIREBASE_CLIENT_EMAIL,
    type: 'service_account',
  });
  console.log('Using env vars');
}

admin.initializeApp({ credential });

const TEST_USERS = [
  { envEmail: 'TEQF_TEST1_EMAIL', envPass: 'TEQF_TEST1_PASS', email: 'teqftest1@tequierofeliz.mx' },
  { envEmail: 'TEQF_TEST2_EMAIL', envPass: 'TEQF_TEST2_PASS', email: 'teqftest2@tequierofeliz.mx' },
  { envEmail: 'XB_TEST1_EMAIL',   envPass: 'XB_TEST1_PASS',   email: 'xbtest1@tequierofeliz.mx'   },
  { envEmail: 'XB_TEST2_EMAIL',   envPass: 'XB_TEST2_PASS',   email: 'xbtest2@tequierofeliz.mx'   },
];

function genPassword() {
  return 'E2e!' + crypto.randomBytes(8).toString('hex');
}

async function main() {
  const auth = admin.auth();
  const lines = ['# Auto-generated by scripts/reset-e2e-passwords.cjs — DO NOT COMMIT', ''];

  for (const u of TEST_USERS) {
    let uid;
    try {
      const record = await auth.getUserByEmail(u.email);
      uid = record.uid;
    } catch (err) {
      console.warn(`⚠️  User not found: ${u.email} (${err.code})`);
      lines.push(`${u.envEmail}=${u.email}`);
      lines.push(`${u.envPass}=USER_NOT_FOUND`);
      lines.push('');
      continue;
    }

    const newPass = genPassword();
    await auth.updateUser(uid, { password: newPass });
    console.log(`✅ ${u.email} — password reset`);

    lines.push(`${u.envEmail}=${u.email}`);
    lines.push(`${u.envPass}=${newPass}`);
    lines.push('');
  }

  const outPath = path.resolve(__dirname, '../tests/e2e/.env.test');
  fs.writeFileSync(outPath, lines.join('\n'), { mode: 0o600 });
  console.log(`\n✅ Credentials written to ${outPath}`);
  console.log('Run tests: npx playwright test tests/e2e/planner-access.spec.ts --reporter=list,html');
}

main().catch(err => { console.error(err); process.exit(1); });
